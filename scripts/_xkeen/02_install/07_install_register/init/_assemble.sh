#!/bin/sh
#
# Сборка S05xkeen из модулей. Используется register_xkeen_initd для генерации
# /opt/etc/init.d/S05xkeen из набора файлов в этой директории.
#
# Файлы конкатенируются в лексикографическом порядке имени.
# Конвенция:
#   <NN>_<topic>.sh           — основной модуль (NN = 00..99)
#   <NN><letter>_<topic>.sh   — подмодуль (например 14a, 14b — части heredoc'а)
#   _<name>.sh                — служебные файлы, не попадают в сборку (подчёркивание)
#
# Использование:
#   sh _assemble.sh > /opt/etc/init.d/S05xkeen
#
# Пути модулей определяются относительно расположения этого скрипта,
# чтобы можно было запускать из любого cwd.

set -e

assemble_dir="$(cd "$(dirname "$0")" && pwd)"

# Glob: имя начинается с цифры (т.е. не служебный _file.sh), оканчивается .sh
for module in "$assemble_dir"/[0-9]*.sh; do
    [ -f "$module" ] || continue
    cat "$module"
done
